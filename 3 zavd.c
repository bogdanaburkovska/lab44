#include <stdio.h>

int main() {
    int i, j;
    int sum = 0;

    for (i = 1; i <= 30; i++) {
        for (j = 1; j <= i; j++) {
            sum += (1 + i * i + j * j);//використовує два вкладені цикли 'for', щоб обчислити суму відповідно до заданої формули. Змінна 'sum' ініціалізується нулем, а потім додається значення
        }
    }

    printf("Sum: %d\n", sum);//В результаті програма виводить обчислену суму на екран.

    return 0;
}
